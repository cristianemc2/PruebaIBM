package osb.rest.common.exception;

public class PortobellaException extends Exception {

    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    public PortobellaException() {
        super();
    }

    public PortobellaException(String message, Throwable cause) {
        super(message, cause);
    }

    public PortobellaException(String message) {
        super(message);
    }

    public PortobellaException(Throwable cause) {
        super(cause);
    }

}
